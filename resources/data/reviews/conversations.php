                    <h2>CONVERSATIONS IN THE LANGUAGE OF JAZZ</h2>

                    <figure>
                        <blockquote>
                            <p>This new compact disk by clarinetist Harry Skoler is quite unique . . . In this age of fusion and MIDI, Skoler's clarinet finds home base on each of these tracks.  His Interpretations and solos take the listener back to the heart of jazz . . . Anchored by Kimball's and Gilmore's solid playing, Skoler and Saindon have produced an excellent CD.</p>
                        </blockquote>
                        <figcaption>- Mark Ford, <cite>Percussive Arts Society</cite></figcaption>

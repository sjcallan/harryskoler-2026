            <h1 style="color:#FFF">About</h1>

            <div class="col_two_third">
                <p>Jazz clarinetist Harry Skoler is Professor of Woodwinds at Boston's prestigious Berklee College of Music. 
                He received his Bachelor of Music degree from Berklee, and a Master of Music degree from  New England Conservatory, where he studied with legendary jazz great Jimmy Giuffre. His recordings include Conversations in the Language of Jazz, Reflections on the Art of Swing - 
                A Tribute to Benny Goodman, A Work of Heart on Brownstone Recordings, and Two Ones on Soliloquy Records. His latest recording is Living In Sound: The Music of Charles Mingus on Sunnyside Records. 
                He has authored articles on jazz education, including a "Pro-Workshop" article in DownBeat magazine, and has been included with a biography and philosophy of artistry in Gianluca Campagnolo's Volume 10 Method Book for Clarinet, which features bios and tips from many prominent clarinetists. 
                Harry contributed to a clarinet method book by Vandoren and published by Carl Fischer entitled The Vandoren Etude & Exercise Book for Clarinet - 
                The Secrets of Ten Master Clarinetists and a jazz saxophone method book by Vandoren and published by Carl Fischer entitled the vandojazz ETUDE AND EXERCISE BOOK FOR SAXOPHONE - THE SECRETS OF TEN MASTER SAXOPHONISTS. He has garnered numerous national and international reviews, 
                including reviews in JazzTimes, DownBeat, and Jazziz among others. Cover features include Strictly Jazz, Jazz Quarterly, Northeast Performer, and Jazz Now magazines, and bio/reviews in AMG's All Music Guide to Jazz. He has been included in articles in JazzTimes, Jazziz and DownBeat Magazines. 
                He has been charted for national radio airplay on JazzWeek and Gavin Magazine, and charted at #8 on Jazziz magazine's Top 40 "Radioactive" chart. Performances include national jazz clubs and festivals, including the Blue Note in NYC, Regattabar and Scullers in Boston, International 
                Clarinet Association’s ClarinetFest in Baton Rouge, Cape May Jazz Festival and tours in Costa Rica, Japan, Mexico and Norway. Harry as a clinician has conducted master classes nationally and internationally, including Costa Rica, Japan, Mexico and Australia. He has participated as a 
                judge for Vandoren’s Emerging Artist Competition, and has been a member of Berklee College of Music’s “Audition & Interview” team, auditioning applicants to the college, traveling nationally and internationally since 2007. Harry has been featured on many national radio interviews 
                and on the nationally televised BET program “Jazz Discovery”. In addition to touring with his group, Harry has given concerts/clinics for young people with the educational ensemble "Adventures With Jazz". Endorsements include Buffet Crampon and Vandoren. 
                He lives in Bradford, MA with his wife Joanne.</p>

